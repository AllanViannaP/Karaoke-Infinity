export class I18n {
  constructor({ storageKey, defaultLanguage = DEFAULT_LANGUAGE } = {}) {
    this.storageKey = storageKey;
    this.language = this.getStoredLanguage() || defaultLanguage;
  }

  get htmlLang() {
    return LANGUAGE_OPTIONS.find((language) => language.code === this.language)?.htmlLang || DEFAULT_LANGUAGE;
  }

  getStoredLanguage() {
    if (!this.storageKey) return "";
    const storedLanguage = localStorage.getItem(this.storageKey);
    return this.isSupported(storedLanguage) ? storedLanguage : "";
  }

  isSupported(language) {
    return Object.prototype.hasOwnProperty.call(TRANSLATIONS, language);
  }

  setLanguage(language) {
    this.language = this.isSupported(language) ? language : DEFAULT_LANGUAGE;
    if (this.storageKey) localStorage.setItem(this.storageKey, this.language);
  }

  t(key, vars = {}) {
    const catalog = TRANSLATIONS[this.language] || TRANSLATIONS[DEFAULT_LANGUAGE];
    const template = catalog[key] || TRANSLATIONS[DEFAULT_LANGUAGE][key] || key;
    return template.replace(/\{(\w+)\}/g, (_, name) => String(vars[name] ?? ""));
  }
}
